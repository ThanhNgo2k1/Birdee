package dto;

/**
 *
 * @author ACE
 */
public class Shipping_Method {
    private int id;
    private String name;
    private double price;

    public Shipping_Method() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Shipping_Method(int id, String name, double price) {
        this.id = id;
        this.name = name;
        this.price = price;
    }

    @Override
    public String toString() {
        return "Shipping_Method{" + "id=" + id + ", name=" + name + ", price=" + price + '}';
    }
    
    
}
